#!/usr/bin/node
let count = 0;              // defino una variable
console.log(++count);       // Aumenta en uno la variable
console.log(count--);       // Decremento en uno la variable
console.log(count += 2);    // suma en 2 la variable
console.log(count *= 3);    // multiplica en 3 la variable
console.log(count);
